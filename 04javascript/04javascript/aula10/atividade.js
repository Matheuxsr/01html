// faça uma função para cada operação matemática
// adição, subtração, divisão e multiplicação

function adicionar (a, b) {
    return a + b
}

let resultado = adicionar(22 + 4)
console.log(adicionar(22, 4));

function subtrair (a, b) {
    return a - b
}
 
let resultado2 = subtrair(22 - 4)
console.log(subtrair(22, 4));

function dividir (a, b) {
    return a / b
}
let resultado3 = dividir(22 / 4)
console.log(dividir(22, 4));

function multiplicar(a, b) {
    return a * b 
}
let resultado4 = multiplicar(22 * 4)
console.log(multiplicar(22, 4));
